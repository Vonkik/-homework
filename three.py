def tochka_v_oblasti_ot_pryamykh(k1, k2, k3, b1, b2, b3, x, y):  # Функция для проверки точки внутри области
    y1 = k1 * x + b1  # Вычисляем значение функции первой прямой
    y2 = k2 * x + b2  # Вычисляем значение функции второй прямой
    y3 = k3 * x + b3  # Вычисляем значение функции третьей прямой

    if (y >= y1 and y <= y2 and y >= y3) or (y <= y1 and y >= y2 and y <= y3):  # Проверка, лежит ли точка между прямыми
        return True  # Если да, возвращаем тру
    return False  # Если нет, возвращаем фолз

k1 = float(input("Введите коэффициент k1: "))  # Ввод коэффициента k1 для первой прямой
k2 = float(input("Введите коэффициент k2: "))  # Ввод коэффициента k2 для второй прямой
k3 = float(input("Введите коэффициент k3: "))  # Ввод коэффициента k3 для третьей прямой
b1 = float(input("Введите коэффициент b1: "))  # Ввод коэффициента b1 для первой прямой
b2 = float(input("Введите коэффициент b2: "))  # Ввод коэффициента b2 для второй прямой
b3 = float(input("Введите коэффициент b3: "))  # Ввод коэффициента b3 для третьей прямой

x = float(input("Введите координату x точки: "))  # Ввод координаты x точки
y = float(input("Введите координату y точки: "))  # Ввод координаты y точки

if tochka_v_oblasti_ot_pryamykh(k1, k2, k3, b1, b2, b3, x, y):  # Проверка, лежит ли точка в области
    print("Точка лежит внутри области.")  # Вывод, если точка в области
else:
    print("Точка не лежит внутри области.")  # Вывод, если точка не в области
